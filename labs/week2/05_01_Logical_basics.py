# Logical Operation_1
# Logical Operation not

a = True
b = False

# print the value of not of a and not of b
print(f'a = {a}, b = {b} , not a = {not a}, not b = {not b}')


# Logical Operation_2
# Logical Operation OR

a = True
b = False

# print the value of a OR b
print(f'a = {a}, b = {b}, a or b = {a or b}')

